<template xmlns:v-slot="http://www.w3.org/1999/XSL/Transform">
  <v-timeline
      align-top
      dense>

    <!-- start status -->
    <v-timeline-item small color="grey" fill-dot>
      <v-layout pt-1>
        <v-flex xs10>
          <!-- empty expansion panel as placeholder -->
          <v-expansion-panel class="elevation-0" disabled>
            <v-expansion-panel-content hide-actions>
              <template v-slot:header>
                <div></div>
              </template>
            </v-expansion-panel-content>
          </v-expansion-panel>
        </v-flex>
      </v-layout>
    </v-timeline-item>

    <v-timeline-item
        color=""
        small
        v-for="n in items"
        :key="n.id"
        :icon="n.status.icon"
    >

      <v-layout pt-1>
        <v-flex xs10 @click="onExpand(n)">
          <v-expansion-panel class="elevation-0" >
            <v-expansion-panel-content>
              <template v-slot:header>
                <strong>{{ n.name }}</strong>
              </template>
              <v-card>
                <v-card-text>
                  <div :id="n.id + `-console`"></div>
                </v-card-text>
              </v-card>
            </v-expansion-panel-content>
          </v-expansion-panel>
        </v-flex>
      </v-layout>

    </v-timeline-item>

    <!-- end status -->
    <v-timeline-item small color="grey"></v-timeline-item>
  </v-timeline>
</template>

<script>
  import { StepWrapper } from '@/util/steps'
  import { Terminal } from 'xterm'

  export default {
    name: 'JobDetailLogs',
    data () {
      return {
        // key=id, value={xterm: object, expended: false}
        config: {}
      }
    },
    props: {
      steps: {
        required: true,
        type: Array
      }
    },
    computed: {
      items: function () {
        const wrapperList = []

        this.steps.forEach((s, index) => {
          const stepWrapper = new StepWrapper(s, index)
          wrapperList.push(stepWrapper)
        })

        this.resetConfig(wrapperList)

        return wrapperList
      }
    },
    methods: {
      resetConfig(wrapperList) {
        this.config = {}

        wrapperList.forEach((s) => {
          this.config[s.id] = {xterm: null, expended: false}
        })
      },

      onExpand (stepWrapper) {
        let stepId = stepWrapper.id
        let instance = this.config[stepId].xterm
        let expanded = this.config[stepId].expended

        this.config[stepId].expended = expanded = !expanded

        if (!expanded) {
          return
        }

        if (!instance) {
          instance = new Terminal()
          instance.open(document.getElementById(stepId + '-console'))
          instance.fit()
          instance.on('scroll', (e) => {
            this.onTermScroll(stepId, e)
          })

          this.config[stepId].xterm = instance
        }
      },

      onTermScroll (stepId, e) {
        console.log(e)
        console.log(stepId)
      },

      // invoked by parent component
      updateStep (newStep) {
        for (let i = 0; i < this.items.length; i++) {
          const item = this.items[i]
          if (item.id === newStep.id) {
            this.items[i].rawStatus = newStep.status
            return
          }
        }
      },

      addLog (logWrapper) {
        let config = this.config[logWrapper.id]

        if (!config || !config.xterm) {
          return
        }

        config.xterm.writeln(logWrapper.log)
      }
    }
  }
</script>

